package org.jetbrains.plugins.scala.lang.completion

/**
 * Represents a Scala keyword. Constants defined in this trait represent all
 * keywords of the Scala language.
 */
object ScalaKeyword {

  val TRUE = "true"  //yes
  val FALSE = "false"     //yes
  val NULL = "null"        //yes
  val ABSTRACT = "abstract"
  val CASE = "case"
  val CATCH = "catch"
  val CLASS = "class"
  val DEF = "def"
  val DO = "do"
  val ELSE = "else"
  val EXTENDS = "extends"
  val FINAL = "final"
  val FINALLY = "finally"
  val FOR = "for"
  val FOR_SOME = "forSome"
  val IF = "if"
  val IMPLICIT = "implicit"
  val IMPORT = "import"
  val LAZY = "lazy"
  val MATCH = "match"
  val NEW = "new"              //yes
  val OBJECT = "object"
  val OVERRIDE = "override"
  val PACKAGE = "package"           //yes
  val PRIVATE = "private"
  val PROTECTED = "protected"
  val REQUIRES = "requires"
  val RETURN = "return"
  val SEALED = "sealed"
  val SUPER = "super"                    //yes
  val THIS = "this"                       //yes
  val THROW = "throw"
  val TRAIT = "trait"
  val TRY = "try"
  val TYPE = "type"
  val VAL = "val"
  val VAR = "var"
  val WHILE = "while"
  val WITH = "with"
  val YIELD = "yield"

}