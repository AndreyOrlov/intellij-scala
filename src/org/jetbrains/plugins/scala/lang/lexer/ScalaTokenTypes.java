package org.jetbrains.plugins.scala.lang.lexer;

import com.intellij.psi.tree.IElementType;
/**
 * Author: Ilya Sergey
 * Date: 24.09.2006
 * Time: 12:39:38
 */
public interface ScalaTokenTypes {

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////// integer and float literals ///////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    IElementType tINTEGER =                     new ScalaElementType("integer literal");

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////// Operators ////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    IElementType tASSGN =                       new ScalaElementType("=");
    IElementType tPLUS =                        new ScalaElementType("+");
    IElementType tMINUS =                       new ScalaElementType("-");
    IElementType tSTAR =                        new ScalaElementType("*");
    IElementType tDIV =                         new ScalaElementType("/");
    IElementType tWHITE_SPACE =                 new ScalaElementType("white space");

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////// Braces ///////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    IElementType tOPEN_PARENTHIS  =             new ScalaElementType("(");
    IElementType tLBRACKET =                    new ScalaElementType("(");
    IElementType tRBRACKET =                    new ScalaElementType(")");

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////// keywords /////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    IElementType kPRINT =                       new ScalaElementType("print");

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////// variables and constants //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    IElementType tIDENTIFIER =                  new ScalaElementType("identifier");


}
